/*Createing tables*/
CREATE TABLE "CUSTOMER"
   (	"ID" NUMBER(19,2) NOT NULL ENABLE,
	"BENEFICIARY" NUMBER(1,0) NOT NULL ENABLE,
	"EMAIL" VARCHAR2(255 CHAR),
	"OBSERVATIONS" VARCHAR2(255 CHAR),
	"PASSWORD" VARCHAR2(255 CHAR),
	"USERNAME" VARCHAR2(255 CHAR),
	 PRIMARY KEY ("ID"),
	CONSTRAINT "UK_USERNAME" UNIQUE ("USERNAME"));

SELECT * from "AUTODATA";

  CREATE TABLE "PROFILE"
   (	"ADDRESS" VARCHAR2(255 CHAR),
	"BIRTHDAY" DATE,
	"GENDER" VARCHAR2(255 CHAR),
	"NAME" VARCHAR2(255 CHAR),
	"PHONENUMBER" VARCHAR2(255 CHAR),
	"SSC" VARCHAR2(255 CHAR),
	"SURNAME" VARCHAR2(255 CHAR),
	"WORK" VARCHAR2(255 CHAR),
	"CUSTOMER_ID" NUMBER(19,2) NOT NULL ENABLE,
	 PRIMARY KEY ("CUSTOMER_ID"),
	 CONSTRAINT "FK_PROFILE_CUSTOMER_ID" FOREIGN KEY ("CUSTOMER_ID")
	  REFERENCES "CUSTOMER" ("ID") ENABLE
   ) SEGMENT CREATION IMMEDIATE;


  CREATE TABLE "AUTODATA"
   (	"ACCIDENTS" NUMBER(2,0),
	"CATEGORY" VARCHAR2(255 CHAR),
	"EXPRIENCE" NUMBER(2,0),
	"LICENSEID" VARCHAR2(255 CHAR),
	"TYPE" VARCHAR2(255 CHAR),
	"CUSTOMER_ID" NUMBER(19,2) NOT NULL ENABLE,
	 PRIMARY KEY ("CUSTOMER_ID"),
	 CONSTRAINT "FK_AUTODATA_CUSTOMER_ID" FOREIGN KEY ("CUSTOMER_ID")
	  REFERENCES "CUSTOMER" ("ID") ENABLE
   ) SEGMENT CREATION IMMEDIATE ;

  CREATE TABLE "TRAVELDATA"
   (	"POURPOSE" VARCHAR2(255 CHAR),
	"CUSTOMER_ID" NUMBER(19,2) NOT NULL ENABLE,
	 PRIMARY KEY ("CUSTOMER_ID"),
	 CONSTRAINT "FK_TRAVELDATA_CUTOMER_ID" FOREIGN KEY ("CUSTOMER_ID")
	  REFERENCES "CUSTOMER" ("ID") ENABLE
   ) SEGMENT CREATION DEFERRED ;
